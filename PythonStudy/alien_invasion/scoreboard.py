# coding=gbk
import json

import pygame.font
from pygame.sprite import Group

from ship import Ship


class Scoreborad():
    """显示得分信息类"""

    def __init__(self, ai_settings, screen, stats):
        """初始化显示得分涉及的属性"""
        self.screen = screen
        self.screen_rect = screen.get_rect()
        self.ai_settings = ai_settings
        self.stats = stats

        #显示得分信息时使用的字体设置
        self.text_color = ( 30, 30, 30 )
        # 将None改成宋体"simsunnsimsun"或其他可以显示中文：
        # print(pygame.font.get_fonts())查看字体名字列表
        self.font = pygame.font.SysFont("None",40)


        # 准备包含最高得分和当前得分的图像
        self.prep_score()
        self.prep_high_score()
        self.prep_level()
        self.prep_ships()

    def prep_score(self):
        """将得分转换为一幅渲染的图像"""
        rounded_score = round(self.stats.score,-1) #圆整到10的倍数
        score_str = "{:,}".format(rounded_score)
        #文本转换成图像，返回的是surface
        self.score_image = self.font.render(score_str,True,self.text_color,self.ai_settings.bg_color)

        #将得分放在屏幕右上角
        self.score_rect = self.score_image.get_rect()
        self.score_rect.right = self.screen_rect.right - 20
        self.score_rect.top = 20

    def prep_level(self):
        """将等级转换为渲染的图像"""
        level_str = "Level: "+ str(self.stats.level)
        self.level_image =  self.font.render( level_str,True,self.text_color,self.ai_settings.bg_color)

        # 将等级放在得分下方
        self.level_rect = self.level_image.get_rect()
        self.level_rect.right = self.score_rect.left - 180
        self.level_rect.top = self.score_rect.top

    def show_score(self):
        """在屏幕上显示得分"""
        self.screen.blit(self.score_image,self.score_rect)
        self.screen.blit(self.high_score_image,self.high_score_rect)
        self.screen.blit(self.level_image,self.level_rect)
        #绘制飞船
        self.ships.draw(self.screen)

    def prep_high_score(self):
        """将最高得分转换为渲染的图像"""
        high_score = round(self.stats.high_score,-1)
        high_score_str = "High Score: "+"{:,}".format(high_score)
        #文本转换成图像,返回的是surface
        self.high_score_image = self.font.render(high_score_str,True,
             self.text_color,self.ai_settings.bg_color)

        #将最高得分放在屏幕顶部中央
        self.high_score_rect = self.high_score_image.get_rect()
        self.high_score_rect.left = self.screen_rect.left + 380
        self.high_score_rect.top = self.score_rect.top

    def prep_ships(self):
        """显示还余下多少艘飞船"""
        self.ships = Group()
        for ship_number in range(self.stats.ships_left):
            ship = Ship(self.ai_settings , self.screen)
            ship.rect.x = 10 + ship_number * ship.rect.width
            ship.rect.y = 10
            self.ships.add(ship)

    def save_score(self,high_score):
        """保存最高分"""
        filename = 'HighScore.json'
        with open(filename,'w') as f_obj:
            json.dump(high_score,f_obj)
